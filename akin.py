# A simple countdown program using a while loop
counter = 10

while counter > 0:
    print("Current count:", counter)
    counter -= 1  # Decrease the counter by 1 each time

print("Done with the countdown!")
#Explanation:
#Setting Up the Counter: I started with a variable counter set to 5.
#Running the Loop: The while loop keeps running as long as counter is more than 0. Each time the loop runs, the counter goes down by 1.
#Finishing the Countdown: When the loop finishes, I print a final message to indicate the countdown is complete.